    public boolean valide(String string) {
        try {
            ResultSet MysqlConsulta = Producto.c.MysqlConsulta("SELECT * FROM  `encabezadosaldoactivvo` WHERE  `indiceRe` LIKE  '"+string+"'");
            if(MysqlConsulta.next()){
                JOptionPane.showMessageDialog(rootPane, "ya existe");
                return false;
            }
        } catch (SQLException ex) {
            Logger.getLogger(GrillaResponsable.class.getName()).log(Level.SEVERE, null, ex);
            return true;
        }
        return true;
    }
