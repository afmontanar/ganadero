    public void center() {
        int sw = 0;
        tm.vaciarTabla();
        try {
            ResultSet resultado1 = Producto.c.MysqlConsulta("select indiceRe,idresponsable,nombrerespon,codedireccion,namedireccion,codearea,namearea,codedepartamento,namedepartamento from responsables where  nombrerespon like'%" + jTextField1.getText() + "%'");

            while (resultado1.next()) // va avanzando de UN(1) cliente.
            {
                String[] columnas = {resultado1.getString("indiceRe"),resultado1.getString("idresponsable"), resultado1.getString("nombrerespon"), resultado1.getString("codedireccion"), resultado1.getString("namedireccion"), resultado1.getString("codearea"), resultado1.getString("namearea"), resultado1.getString("codedepartamento"), resultado1.getString("namedepartamento")};
                tm.ingresarUsuario(columnas);
                sw = 1;
            }
            if (sw == 0) {
                JOptionPane.showMessageDialog(this, "NO existe el Responsable");

            }
            resultado1.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "error al buscar los datos" + e);
        }
    }

